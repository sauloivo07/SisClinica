    private void tabelaMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMedicosMouseClicked
        if(evt.getClickCount() == 2){
            int linha = tabelaMedicos.getSelectedRow();
            txtId.setText(String.valueOf(tabelaMedicos.getValueAt(linha, 0)));
            txtNomeMedico.setText((String) tabelaMedicos.getValueAt(linha, 1));
            jFormattedCRM.setText((String) tabelaMedicos.getValueAt(linha, 2));
            comBoxEspecialista.setSelectedItem((String) tabelaMedicos.getValueAt(linha, 3));
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnCancelar.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnNovo.setEnabled(false);
            estadoCampos(true);
        }
    }//GEN-LAST:event_tabelaMedicosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Medicos m = new Medicos();
        String msgErro = "";
        m.setId(Integer.parseInt(txtId.getText()));
        
        if(txtNomeMedico.getText().equals("")) msgErro += "Campo Medico Vazio!\n";
        else m.setNome(txtNomeMedico.getText());
        
        if(comBoxEspecialista.getSelectedItem().equals("Selecionar")) msgErro += "Campo Especialidade Invalido!\n";
        else m.setEspecialidade((String) comBoxEspecialista.getSelectedItem());
        
        if(jFormattedCRM.getText().equals("")) msgErro += "Campo CRM Vazio!\n";
        else m.setCrm(Integer.parseInt(jFormattedCRM.getText()));
        
        if(msgErro.length() == 0){
            MDao.atualizar(m);
            popularTabela(MDao.consultar());
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);
            btnNovo.setEnabled(true);
            estadoCampos(false);
            limparCampos();
        }else{
            JOptionPane.showMessageDialog(null, msgErro);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Medicos m = new Medicos();
        int resposta;
        resposta = JOptionPane.showConfirmDialog(null, "Realmente deseja exluir medico?");
        if(resposta == JOptionPane.YES_OPTION){
        m.setId(Integer.parseInt(txtId.getText()));
        MDao.deletar(m);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnCancelar.setEnabled(false);
            btnNovo.setEnabled(true);
            estadoCampos(false);
            limparCampos();
            lista = MDao.consultar();
            popularTabela(lista);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

