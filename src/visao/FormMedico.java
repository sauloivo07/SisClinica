    private void txtPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarKeyReleased
        String nome = txtPesquisar.getText();
        
        if(nome.length() > 0){
            listaAux.clear();
            for (Medicos m : lista) {
                if(m.getNome().toLowerCase().startsWith(nome.toLowerCase())){
                    listaAux.add(m);
                }
            }
            popularTabela(listaAux);
        }else{
            popularTabela(lista);
        }
    }//GEN-LAST:event_txtPesquisarKeyReleased

    private void criarTabela() {

        dt = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int mColIndex) {
                return false;
            }
        };
       tabelaMedicos.setModel(dt);
       dt.addColumn("Id");
       dt.addColumn("Medico");
       dt.addColumn("CRM");
       dt.addColumn("Especialidade");
       tabelaMedicos.getColumnModel().getColumn(0).setPreferredWidth(10);
       tabelaMedicos.getColumnModel().getColumn(0).setResizable(false);
       tabelaMedicos.getColumnModel().getColumn(1).setPreferredWidth(150);
       tabelaMedicos.getColumnModel().getColumn(1).setResizable(false);
       tabelaMedicos.getColumnModel().getColumn(2).setPreferredWidth(50);
       tabelaMedicos.getColumnModel().getColumn(2).setResizable(false);
       tabelaMedicos.getColumnModel().getColumn(3).setPreferredWidth(150);
       tabelaMedicos.getColumnModel().getColumn(3).setResizable(false);
       tabelaMedicos.getTableHeader().getReorderingAllowed();
    }
    
    public void popularTabela(List<Medicos> lista) {
        dt.setNumRows(0);
        
        for(Medicos m : lista){
            dt.addRow(new Object[]{m.getId(), m.getNome(), String.valueOf(m.getCrm()), String.valueOf(m.getEspecialidade())});
        }
    }
    
