    private void montarTabela(){
        dt = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int rowIndex, int mColIndex) {
                return false;
            }
        };
        dt.addColumn("Nome");
        dt.addColumn("Telefone");
        dt.addColumn("Rg");
        dt.addColumn("Bairro");
        
        tabelaPacientes.setModel(dt);
        tabelaPacientes.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabelaPacientes.getColumnModel().getColumn(0).setResizable(false);
        tabelaPacientes.getColumnModel().getColumn(1).setPreferredWidth(30);
        tabelaPacientes.getColumnModel().getColumn(1).setResizable(false);
        tabelaPacientes.getColumnModel().getColumn(2).setPreferredWidth(30);
        tabelaPacientes.getColumnModel().getColumn(2).setResizable(false);
        tabelaPacientes.getColumnModel().getColumn(3).setPreferredWidth(20);
        tabelaPacientes.getColumnModel().getColumn(3).setResizable(false);
        tabelaPacientes.getTableHeader().setReorderingAllowed(false);
        populaTabela();
    }
    
    public void populaTabela(){
        dt.setNumRows(0);
        listaPacientes = pDao.listar();
        
        for (Pacientes p : listaPacientes) {
            dt.addRow(new Object[]{p.getNome(), p.getTel(), p.getRg(), p.getBairro().getNome()});
        }
    }
    
    public void populaComboBox(){
        listaMedicos = mDao.consultar();
        
        comboBoxMedico.removeAllItems();
        comboBoxMedico.addItem("Selecione");
        for (Medicos m : listaMedicos) {
            comboBoxMedico.addItem(m.getNome());
        }
    }
    
    private void limpaCampos(){
        txtPaciente.setText("");
        comboBoxTurno.setSelectedIndex(0);
        comboBoxMedico.setSelectedIndex(0);
        dataChooser.setCalendar(null);
        txtMotivo.setText("");
    }
    
    private boolean verificaCampos(){
        if(txtPaciente.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo paciente vazio");
            return true;
        }
        if(comboBoxTurno.getSelectedItem().equals("Selecione")){
            JOptionPane.showMessageDialog(null, "Campo turno invalido");
            return true;
        }
        if(comboBoxMedico.getSelectedItem().equals("Selecione")){
            JOptionPane.showMessageDialog(null, "Campo medico invalido");
            return true;
        }
            
        return false;
    }
    
    private boolean campoValido(String nome){
        
        for (Pacientes p : listaPacientes) {
            if(p.getNome().equals(nome)){
                return true;
            }
        }
        return false;
    }
    
   
    
